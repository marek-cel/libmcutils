% https://en.wikipedia.org/wiki/PID_controller#Series/interacting_form

k = 1.0
tau_i = 2.0
tau_d = 3.0

alpha = 1 + tau_d / tau_i

kp = k * alpha
ti = tau_i * alpha
td = tau_d / alpha

p1 = pidstd( kp, ti, td )